## Math in python

# Arithmetic operators
friends = 10
friends = friends + 1
print(f"I have {friends} friend(s)")
friends += 1
print(f"I have {friends} friend(s)")
friends -= 2
print(f"I have {friends} friend(s)")
friends *= 3
print(f"I have {friends} friend(s)")
friends /= 2
print(f"I have {friends} friend(s)")
friends **= 2
print(f"I have {friends} friend(s)")
remainder = friends % 7
print(f"remainder: {remainder}")

# Built-in functions
x = 3.14
y = 4
z = 5

result = round(x)
print(result)

result = abs(y)
print(result)

result = pow(4,3)
print(result)

result = max(x, y, z)
print(result)

result = min(x, y, z)
print(result)
